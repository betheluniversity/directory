class Person:

    def __init__(self, first_name, last_name, email, living, po, role, picture=None, home=None, groups=None):
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.living = living
        self.po = po
        self.role = role
        self.home = home
        self.picture = picture
        self.groups = groups
